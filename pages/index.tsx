import type { NextPage, GetStaticProps, InferGetStaticPropsType } from 'next'
import type { TImages, ImageInterface } from '../types/Images'
import Head from 'next/head'
import Header from '../components/common/Header'
import HomeContainer from '../components/home/Container'
import {getAllImages} from '../utils/photos'

type HomeProps = {
  images: ImageInterface[]
}

const Home: NextPage = ({
  images,
}: HomeProps | InferGetStaticPropsType<typeof getStaticProps>) => {
  return (
    <main>
      <Head>
        <title>NextGalleria | by mrLuisFer</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      <HomeContainer images={images} />
    </main>
  )
}
export default Home

export const getStaticProps: GetStaticProps = async () => {
  const imagesData: ImageInterface[] = getAllImages()

  return {
    props: {
      images: imagesData,
    },
  }
}
